const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem(STORAGE_KEY) || 'uz');

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.title = `${dictionaries[language].appName} - ${dictionaries[language].appSubtitle}`;
  }, [language]);

  const value = useMemo(() => {
    const translate = (key) => dictionaries[language][key] || dictionaries.uz[key] || key;
    const translateRole = (role) => {
      const map = { Admin: 'admin', Clinician: 'clinician', Receptionist: 'receptionist' };
      return translate(map[role] || role);
    };
    const translateGender = (gender) => {
      const map = { Male: 'male', Female: 'female', Other: 'other' };
      return translate(map[gender] || gender);
    };
    const translateSeverity = (severity) => translate(severity || 'notAvailable');
    const translateShift = (shift) => {
      const map = { day: 'dayShift', night: 'nightShift', rotating: 'rotatingShift' };
      return translate(map[shift] || shift || 'notAvailable');
    };

    return {
      language,
      setLanguage,
      t: translate,
      trRole: translateRole,
      trGender: translateGender,
      trSeverity: translateSeverity,
      trShift: translateShift,
      locale: language === 'ru' ? 'ru-RU' : 'uz-UZ',
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used inside LanguageProvider');
  }
  return context;
}
